            <p><b><h1>Titulo Primero</h1></b></p>
            <p><b>Disposiciones Generales</b></p>
            <p><b><h1>Capitulo 1</h1></b></p>
            <p><b>Objeto de la Ley</b></p>
            <p>
                <b>Artículo 1.</b> La presente Ley es de orden público e interés social y de observancia general en todo el 
territorio nacional, en términos de lo dispuesto en el párrafo décimo séptimo del artículo 4o. y 73, fracción 
XXIX-C, de la Constitución Política de los Estados Unidos Mexicanos, en materia de movilidad y 
seguridad vial, y tiene por objeto establecer las bases y principios para garantizar el derecho a la 
movilidad en condiciones de seguridad vial, accesibilidad, eficiencia, sostenibilidad, calidad, inclusión e 
igualdad.
            </p>
            <p>La presente Ley tendrá por objetivos: </p>
            <p>
                
<b>I.</b> Sentar las bases para la política de movilidad y seguridad vial, bajo un enfoque sistémico 
y de sistemas seguros, a través del Sistema Nacional de Movilidad y Seguridad Vial y la 
información proporcionada por el Sistema de Información Territorial y Urbano para 
priorizar el desplazamiento de las personas, particularmente de los grupos en situación 
de vulnerabilidad, así como bienes y mercancías, con base en la jerarquía de la 
movilidad señalada en esta Ley, que disminuya los impactos negativos sociales, de 
desigualdad, económicos, a la salud, y al medio ambiente, con el fin de reducir muertes y 
lesiones graves ocasionadas por siniestros viales, para lo cual se debe preservar el 
orden y seguridad vial.
            </p>
            <p>
                <b>II.</b> Definir mecanismos de coordinación de las autoridades de los tres órdenes de gobierno y 
la sociedad en materia de movilidad y seguridad vial;
            </p>
            <p>
                <b>III.</b> Establecer la concurrencia entre la Federación, las entidades federativas, los municipios 
y las demarcaciones territoriales de la Ciudad de México, en el ámbito de sus respectivas 
competencias, en materia de movilidad y seguridad vial, así como los mecanismos para 
su debida coordinación, de conformidad con lo establecido en el artículo 73, fracción 
XXIX-C, de la Constitución Política de los Estados Unidos Mexicanos;
            </p>
            <p>
                <b>IV.</b> Establecer las bases para la coordinación entre integrantes del Sistema Nacional de 
Movilidad y Seguridad Vial a través de los planes de desarrollo, la política de movilidad y 
de seguridad vial con un enfoque integral a la política de desarrollo urbano y 
ordenamiento territorial, que sea transversal con las políticas sectoriales aplicables;
            </p>
            <p>
                <b>V.</b> Determinar mecanismos y acciones que promuevan y fomenten la sensibilización, la 
formación y la cultura de la movilidad y seguridad vial, que permitan el ejercicio pleno de 
este derecho;
            </p>
            <p>
                <b>VI.</b> Vincular la política de movilidad y seguridad vial, con un enfoque integral de la política de 
ordenamiento territorial y desarrollo urbano y de manera transversal con las políticas 
sectoriales aplicables;
            </p>
